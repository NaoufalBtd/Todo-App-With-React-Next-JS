import axios from "axios";
import type { NextPage } from "next";
import { getSession, useSession } from "next-auth/react";

import HomeTemplate from "@/app/components/templates/Home";
import LoadingTemplate from "@/app/components/templates/Loading";
import LoginTemplate from "@/app/components/templates/Login";
import Head from "next/head";

const Home: NextPage = ({ tasks }: any) => {
  tasks = JSON.parse(tasks);
  const { data: session, status } = useSession();
  if (status === "loading") return <LoadingTemplate />;
  if (!session) return <LoginTemplate />;
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <body>
        <HomeTemplate tasks={tasks} />
      </body>
    </>
  );
};

export async function getServerSideProps({ req }: { req: any }) {
  try {
    const session = await getSession({ req });
    let tasks;
    if (session?.user) {
      const res = await axios.post(`${process.env.SITE_URL}/api/lists/home`, {
        ownerId: session.user.id,
      });
      tasks = JSON.stringify(res.data.payload || []);
    } else {
      tasks = "[]";
    }
    return { props: { tasks } };
  } catch (error) {
    console.error(error);
    return { props: { tasks: "[]" } };
  }
}

export default Home;
